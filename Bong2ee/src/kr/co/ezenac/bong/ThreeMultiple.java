package kr.co.ezenac.bong;

public class ThreeMultiple {

	public static void main(String[] args) {
		// 1~100까지 숫자 중 3의 배수 출력
		
		int i = 1;
			if (i % 3 == 0) {
				System.out.println(i);
		}
		int i2 = 2;
		if (i2 % 3 == 0) {
			System.out.println(i2);
		}
		int i3 = 3;
		if (i3 % 3 == 0) {
			System.out.println(i3);
		}
	}

}
